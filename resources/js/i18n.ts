export const LANGUAGE_STORAGE_KEY = 'aig-app-language';
const SUPPORTED_LANGUAGES = ['fr', 'en', 'de'] as const;

const detectInitialLanguage = (): string => {
  if (typeof window === 'undefined') {
    return 'fr';
  }

  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (stored && SUPPORTED_LANGUAGES.includes(stored as (typeof SUPPORTED_LANGUAGES)[number])) {
    return stored;
  }

  const browser = window.navigator.language?.slice(0, 2).toLowerCase();
  if (browser && SUPPORTED_LANGUAGES.includes(browser as (typeof SUPPORTED_LANGUAGES)[number])) {
    return browser;
  }

  return 'fr';
};

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: detectInitialLanguage(),
    fallbackLng: 'fr',
    supportedLngs: SUPPORTED_LANGUAGES as unknown as string[],
    interpolation: {
      escapeValue: false,
    },
  });

export default i18n;